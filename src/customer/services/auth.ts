
import {

  getAuth,

  createUserWithEmailAndPassword,

  signInWithEmailAndPassword,

  signOut,

  onAuthStateChanged

} from "firebase/auth";

import {

  getDatabase,

  ref,

  set,

  get

} from "firebase/database";

////////////////////////////////////////////////////////////////////////////////
// AUTH
////////////////////////////////////////////////////////////////////////////////

const auth = getAuth();

const db = getDatabase();

////////////////////////////////////////////////////////////////////////////////
// REGISTER
////////////////////////////////////////////////////////////////////////////////

export async function registerUser(

  name:string,

  email:string,

  password:string

){

  try {

    //////////////////////////////////////////////////
    // CREATE ACCOUNT
    //////////////////////////////////////////////////

    const userCredential =

      await createUserWithEmailAndPassword(

        auth,

        email,

        password
      );

    //////////////////////////////////////////////////
    // USER
    //////////////////////////////////////////////////

    const user =
      userCredential.user;

    //////////////////////////////////////////////////
    // SAVE DATABASE
    //////////////////////////////////////////////////

    await set(

      ref(db, `users/${user.uid}`),

      {

        uid:
          user.uid,

        name:
          name,

        email:
          user.email,

        role:
          "customer",

        createdAt:
          Date.now()
      }
    );

    //////////////////////////////////////////////////
    // SUCCESS
    //////////////////////////////////////////////////

    return {

      success:true,

      user
    };

  } catch(error:any){

    return {

      success:false,

      error:error.message
    };
  }
}

////////////////////////////////////////////////////////////////////////////////
// LOGIN
////////////////////////////////////////////////////////////////////////////////

export async function loginUser(

  email:string,

  password:string

){

  try {

    //////////////////////////////////////////////////
    // LOGIN
    //////////////////////////////////////////////////

    const userCredential =

      await signInWithEmailAndPassword(

        auth,

        email,

        password
      );

    //////////////////////////////////////////////////
    // USER
    //////////////////////////////////////////////////

    const user =
      userCredential.user;

    //////////////////////////////////////////////////
    // GET PROFILE
    //////////////////////////////////////////////////

    const snapshot =

      await get(

        ref(db, `users/${user.uid}`)
      );

    //////////////////////////////////////////////////
    // DATA
    //////////////////////////////////////////////////

    const profile =
      snapshot.val();

    //////////////////////////////////////////////////
    // SUCCESS
    //////////////////////////////////////////////////

    return {

      success:true,

      user,

      profile
    };

  } catch(error:any){

    return {

      success:false,

      error:error.message
    };
  }
}

////////////////////////////////////////////////////////////////////////////////
// LOGOUT
////////////////////////////////////////////////////////////////////////////////

export async function logoutUser(){

  try {

    await signOut(auth);

    return {

      success:true
    };

  } catch(error:any){

    return {

      success:false,

      error:error.message
    };
  }
}

////////////////////////////////////////////////////////////////////////////////
// CURRENT USER
////////////////////////////////////////////////////////////////////////////////

export function listenAuth(

  callback:any

){

  return onAuthStateChanged(

    auth,

    async(user)=>{

      //////////////////////////////////////////////////
      // NO USER
      //////////////////////////////////////////////////

      if(!user){

        callback(null);

        return;
      }

      //////////////////////////////////////////////////
      // GET PROFILE
      //////////////////////////////////////////////////

      const snapshot =

        await get(

          ref(db, `users/${user.uid}`)
        );

      //////////////////////////////////////////////////
      // PROFILE
      //////////////////////////////////////////////////

      callback({

        uid:
          user.uid,

        ...snapshot.val()
      });
    }
  );
}

